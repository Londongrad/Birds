namespace Birds.UI.Services
{
    /// <summary>
    /// Определяет контракт для моделей представления (ViewModel),
    /// которым требуется выполнить асинхронную инициализацию при переходе
    /// на соответствующее представление.
    ///
    /// Используется навигационным сервисом для вызова загрузки данных
    /// или другой подготовки, которая должна выполняться один раз
    /// или при каждом переходе к экрану.
    /// </summary>
    public interface IAsyncNavigatedTo
    {
        /// <summary>
        /// Метод, вызываемый навигационным сервисом после перехода
        /// на данную модель представления. Должен содержать асинхронную
        /// логику инициализации (например, загрузку данных).
        /// </summary>
        Task OnNavigatedToAsync();
    }
}